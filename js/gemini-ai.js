function generateAIQuestions() {

  const text = document.getElementById("aiText").value.trim();

  if (!text) {
    alert("اكتبي نصًا أولًا 🌷");
    return;
  }

  const prompt = `
أنت معلم لغة عربية خبير للمرحلة الابتدائية.

أنشئ الأسئلة الآتية، واكتب بعد كل سؤال مباشرة:
الإجابة النموذجية:

- 3 أسئلة فهم واستيعاب مع الإجابة النموذجية.
- 2 أسئلة مفردات مع الإجابة النموذجية.
- 2 سؤال قواعد مرتبطان مباشرة بالنص مع الإجابة النموذجية.
- يجب أن تكون إجابات أسئلة القواعد مستخرجة من النص نفسه.
- يُمنع إنشاء أسئلة قواعد عامة خارج النص.
- ركّز على مهارات الصف الخامس الابتدائي في اللغة العربية.
- عند إنشاء أسئلة القواعد، استخرج كلمات أو تراكيب حقيقية من النص.
- سؤال تفكير ناقد مع الإجابة النموذجية.
- سؤال إملاء مع الإجابة النموذجية.

رتّب الناتج بهذا الشكل لكل سؤال:
السؤال:
الإجابة النموذجية:

حول النص التالي:

${text}
`;

  const url =
    "https://chat.openai.com/?prompt=" +
    encodeURIComponent(prompt);

  window.open(url, "_blank");
}
